package basicprograms;

public class Static_Class
{
	
	static void method1()
	{
		System.out.println("God of good fortune");
	}
	
	static void method2()
	{
		System.out.println("Is coming home in September");
	}
	
	static void ganesh()
	{
		System.out.println("Ganapathi");
	}
	
	static void method3()
	{
		System.out.println("bappa");
	}
	static void method4()
	{
		System.out.println("Morya");
	}
	
	
	public static void main(String[] args)
	{
		method1();
		method2();
		ganesh();
		method3();
		method4();
		System.out.println("Happy Ganesha Chathurthi");
		
	}

}